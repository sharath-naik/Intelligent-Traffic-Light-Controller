void main()
{
	DDRA=0x00;	//PORTA I/P Ports
	DDRD=0xFF;	//PORTD O/P Ports
	DDRC=0xFF;	//PORTC O/P Ports
	PORTA=0xFF;	//Pull up resistor enabled
	DDRB=(0<<PD0);// | (1<<PD2);
	PORTB=0x04;
	MCUCSR = (1<<ISC2);	//INT2 at falling edge
	GICR = (1<<INT2);//Enable INT0
	sei();			//Global interrupt enable
	int flag,i,st[4];
	while(1)
	{
		PORTD=0b00100001;
		PORTC=0b00010010;
		timer1_delay(1);
		flag=0;;
		x=PINA;
		x=x^0xFF;
		for(i=0;i<4;i++)
		{
			st[i]=check(i);
			if(st[i]==1)
			flag++;
		}
		if(flag==1)
		one();
		else if(flag==2)
		two(st);
		else if(flag==3)
		three(st);
		else if(flag==4)
		four();
	}
}
