void timer1_delay(int i)
{
	int a;
	TCCR1B |= (1 << CS12)|(1 << CS10);	//1024 prescalar
	a=(i*1000000)/1024;	//Load value calculation
	a=65536-a;
	a--;
	TCNT1=a;

	while(TCNT1!=0xFFFF);
}
		
ISR(INT2_vect)
{
	int a,b,c;
	a=TCNT1;
	b=PORTD;
	c=PORTC;
	if(PORTC==0b01000010)
		{
			PORTC=0b00100010;
			timer1_delay(1);
		}
	else if(PORTC==0b00011000)
		{
			PORTC=0b00010100;
			timer1_delay(1);
		}
	PORTC=0b00010010;
	PORTD=0b01000010;
	timer1_delay(1);
	PORTD=0b10001000;
	timer1_delay(6);
	PORTD=0b01000010;
	timer1_delay(1);
	TCNT1=a;
	PORTD=b;
	PORTC=c;
}
int check(int i)
{
	int a=0;
	if(i==0)
		{
			if((x & 0x01) ==1)
			{
				a++;
			}
		}
	else if(i==1)
		{
			if((x & 0x04) == 0x04)
			{
				a=1;
			}
		}
	else if(i==2)
		{
			if((x & 0x10) == 0x10)
			{
				a=1;
			}
		}
	else if(i==3)
		{
			if((x & 0x40) == 0x40)
			{
				a=1;
			}
		}
	return a;
}
